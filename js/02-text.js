function drawText(s,x,y,color,scale){
  scale=scale||1; ctx.fillStyle=color;
  for(let i=0;i<s.length;i++){
    const g=FONT[s[i]]||FONT[' '];
    for(let r=0;r<5;r++)for(let c=0;c<3;c++)
      if(g[r][c]==='1') ctx.fillRect(x+i*4*scale+c*scale, y+r*scale, scale, scale);
  }
}
function textW(s,scale){return s.length*4*(scale||1)-(scale||1);}
function wrap(s,maxChars){
  const words=s.split(' '), lines=[]; let cur='';
  for(const w of words){
    if((cur+(cur?' ':'')+w).length<=maxChars) cur+=(cur?' ':'')+w;
    else{ if(cur)lines.push(cur); cur=w; }
  }
  if(cur)lines.push(cur);
  return lines;
}

